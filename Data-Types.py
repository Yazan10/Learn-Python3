# type()
# All data in Python is Object

print(type(100))
print(type(5))
print(type(10))
print(type(100.99))
print(type(-100.99))
print(type('hello,world'))
print(type([1,2,3,4,5,6]))
print(type((1,2,3,4,5,6)))
print(type({1:'one',2:'two',3:'three',4:'four',5:'five',6:'six'}))

print(type(2==2))
print(type(2==4))
