
# Escape sequences characters
# \b => Back Space
# \newline => New Line + \
# \\ => Escape Back Slash
# \' => Escape Single Quote
# \" => Escape Double Quotes
# \n => Line Feed
# \r => Carriage Return
# \t => Horizontal Tab
# \xhh => Character Hex Value
# ---------------------

# Back space
print('Hello\bworld') # will remove o

# Escape New Line + back Slash
print('Hello \
i love \
python')

# Escape Back Slash
print(' I love Back Slash \\')

# Escape Single Quote
print('I love Single Quote \'Yazan\'')

# Escape Double Quotes
print('I love Single Quote \"Yazan\"')

# Line Feed
print("Hello World\nSecond Line")

#carriage return
print("123456789\rYazan")

# Horizontal Tab
print("Hello\tworld")

# Character Hex Value
print("\x4F\x73")
